package com.se.domain;

public class result {
	private int exid;
	private String question;
	private String choose_id;
	private String choose_text;
	private String right_id;
	private String right_text;
	public int getExid() {
		return exid;
	}
	public void setExid(int exid) {
		this.exid = exid;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getChoose_id() {
		return choose_id;
	}
	public void setChoose_id(String choose_id) {
		this.choose_id = choose_id;
	}
	public String getChoose_text() {
		return choose_text;
	}
	public void setChoose_text(String choose_text) {
		this.choose_text = choose_text;
	}
	public String getRight_id() {
		return right_id;
	}
	public void setRight_id(String right_id) {
		this.right_id = right_id;
	}
	public String getRight_text() {
		return right_text;
	}
	public void setRight_text(String right_text) {
		this.right_text = right_text;
	}
}
